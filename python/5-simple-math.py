first = int(input("What is the first number? "))
second = int(input("What is the second number? "))

print(
    f"{first} + {second} = {first + second}",
    f"{first} - {second} = {first - second}",
    f"{first} * {second} = {first * second}",
    f"{first} / {second} = {first / second}",
    sep = "\n"
)